#Iterator List Program

#Defining the lists to be iterated
list1 = ['Mumbai','Pune','Delhi','Patna']
list2 = ['Python','PERL','C','C++','Java']

#List iteration 
for city in list1:
    print(city)
    print("\n")
    
for language in list2:
    print(language)
    print("\n")

for i in "iteration is easy":
    print(i, end = " ")
    
